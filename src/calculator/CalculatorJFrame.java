    private void jButton_equalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_equalActionPerformed
        String s = jTextField_Display2.getText(); //store display 2 string
        num2 = Double.parseDouble(jTextField_Display1.getText()); //assign num2 value;
        jTextField_Display2.setText(s + num2); //show the equation
        switch (operator) //calculator operation using switch case 
        {
            case "+": //for addition
                ans = num1 + num2;
                jTextField_Display1.setText(Double.toString(ans));
                break;
            case "-": //for subtruction
                if (jTextField_Display1.getText().startsWith("-")) //checking if unary minus is used
                {
                    ans = num1;
                    jTextField_Display1.setText(Double.toString(ans));
                } else {
                    ans = num1 - num2;
                    jTextField_Display1.setText(Double.toString(ans));
                }

                break;
            case "x": //for multiplication
                ans = num1 * num2;
                jTextField_Display1.setText(Double.toString(ans));
                break;
            case "/": //for divide
                if (num2 != 0) //checking if number 2 is 0
                {
                    ans = num1 / num2;
                    jTextField_Display1.setText(Double.toString(ans));
                } else {
                    jTextField_Display1.setText("Error");
                }
                break;
            case "âˆš": //for square root
                ans = Math.sqrt(num1);
                jTextField_Display1.setText(Double.toString(ans));
                jTextField_Display2.setText(s);
                break;
            case "sin": //for sin theta
                num1 = Math.toRadians(num1); //converting degree into radian
                ans = Math.sin(num1);
                jTextField_Display1.setText(Double.toString(ans));
                jTextField_Display2.setText(s);
                break;
            case "cos": //for cos theta
                num1 = Math.toRadians(num1); //converting degree into radian
                ans = Math.cos(num1);
                jTextField_Display1.setText(Double.toString(ans));
                jTextField_Display2.setText(s);
                break;
            case "tan": //for tan theta
                if (num1 != 90) //checking if number 1 is 90 degree
                {
                    num1 = Math.toRadians(num1); //converting degree into radian
                    ans = Math.tan(num1);
                    jTextField_Display1.setText(Double.toString(ans));
                    jTextField_Display2.setText(s);
                } else {
                    jTextField_Display1.setText("Error");
                    jTextField_Display2.setText(s);
                }
                break;
            case "log": //for logarithm                
                ans = Math.log10(num1); //10 base logarithm
                jTextField_Display1.setText(Double.toString(ans));
                jTextField_Display2.setText(s);
                break;
            case "^": //for power function
                ans = Math.pow(num1, num2);
                jTextField_Display1.setText(Double.toString(ans));
                break;

        }
    }//GEN-LAST:event_jButton_equalActionPerformed

