    private void jButton_minusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_minusActionPerformed
        if (jTextField_Display1.getText().equals("")) {
            jTextField_Display1.setText("-"); //showing unary minus
            num1 = Double.parseDouble(jTextField_Display1.getText()); //assigning value to number 1
            operator = "-";
        } else {
            num1 = Double.parseDouble(jTextField_Display1.getText()); //assigning value to number 1 
            jTextField_Display2.setText(num1 + " - "); //showing value to display 2
            jTextField_Display1.setText(""); //making display 1 empty
            operator = "-";
        }

    }//GEN-LAST:event_jButton_minusActionPerformed

