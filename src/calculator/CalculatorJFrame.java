    private void jButton_2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_2ActionPerformed
        String s = jTextField_Display1.getText(); //Getting previous digits from display1
        if (s.startsWith("0")) //if "0" is pressed first before any digits
        {
            if (s.startsWith("0.")) //if it is 0 point something(0.05)
            {
                jTextField_Display1.setText(s + "2");
            } else {
                jTextField_Display1.setText("2"); //Show only current digit
            }
        } else {
            jTextField_Display1.setText(s + "2"); //Showing current digit with previous digits
    }//GEN-LAST:event_jButton_2ActionPerformed
    }
